     */
    public function update(Request $request, $id)
    {
        $usuario = User::find($id);
        $request->validate([
            'name' => ['required', 'max:255', Rule::unique('users', 'name')->ignore($usuario->id, 'id')],
            'email' => ['required', 'max:255', Rule::unique('users', 'email')->ignore($usuario->id, 'id')]
        ]);
        $usuario->update($request->all());
//        dd($usuario);
        return redirect()->route('profile.index')->with('success', 'Perfil Actualizado');
    }

